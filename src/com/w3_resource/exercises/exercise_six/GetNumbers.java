package com.w3_resource.exercises.exercise_six;

class GetNumbers {
    public int firNum=0;
    public int secNum=0;

    public GetNumbers(int firNum, int secNum) {
        this.firNum = firNum;
        this.secNum = secNum;
    }

    public int getFirNum() { return firNum; }

    public int getSecNum() { return secNum; }
}
